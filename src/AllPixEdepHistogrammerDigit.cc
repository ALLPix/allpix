#include "AllPixEdepHistogrammerDigit.hh"

G4Allocator<AllPixEdepHistogrammerDigit> AllPixEdepHistogrammerDigitAllocator;

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixEdepHistogrammerDigit::AllPixEdepHistogrammerDigit()
{
	pixelIDX = -1;
	pixelIDY = -1;
	pixelCounts = 0;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixEdepHistogrammerDigit::~AllPixEdepHistogrammerDigit()
{;}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixEdepHistogrammerDigit::AllPixEdepHistogrammerDigit(const AllPixEdepHistogrammerDigit& right)
: AllPixDigitInterface()
{
	pixelIDX = right.pixelIDX;
	pixelIDY = right.pixelIDY;
	pixelCounts = right.pixelCounts;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

const AllPixEdepHistogrammerDigit& AllPixEdepHistogrammerDigit::operator=(const AllPixEdepHistogrammerDigit& right)
{

	pixelIDX = right.pixelIDX;
	pixelIDY = right.pixelIDY;
	pixelCounts = right.pixelCounts;
	return *this;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

int AllPixEdepHistogrammerDigit::operator==(const AllPixEdepHistogrammerDigit& right) const
		{
	return ((pixelIDX==right.pixelIDX)&&
			(pixelIDY==right.pixelIDY)&&
			(pixelCounts==right.pixelCounts));
		}

