#include "AllPixTruthWithLorentzDigit.hh"

G4Allocator<AllPixTruthWithLorentzDigit> AllPixTruthWithLorentzDigitAllocator;

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixTruthWithLorentzDigit::AllPixTruthWithLorentzDigit()
{
	pixelIDX = -1;
	pixelIDY = -1;
	pixelCounts = 0;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixTruthWithLorentzDigit::~AllPixTruthWithLorentzDigit()
{;}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

AllPixTruthWithLorentzDigit::AllPixTruthWithLorentzDigit(const AllPixTruthWithLorentzDigit& right)
: AllPixDigitInterface()
{
	pixelIDX = right.pixelIDX;
	pixelIDY = right.pixelIDY;
	pixelCounts = right.pixelCounts;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

const AllPixTruthWithLorentzDigit& AllPixTruthWithLorentzDigit::operator=(const AllPixTruthWithLorentzDigit& right)
{

	pixelIDX = right.pixelIDX;
	pixelIDY = right.pixelIDY;
	pixelCounts = right.pixelCounts;
	return *this;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

int AllPixTruthWithLorentzDigit::operator==(const AllPixTruthWithLorentzDigit& right) const
		{
	return ((pixelIDX==right.pixelIDX)&&
			(pixelIDY==right.pixelIDY)&&
			(pixelCounts==right.pixelCounts));
		}

